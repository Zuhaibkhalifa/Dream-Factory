package com.kodnest.java.string.replacearray;

public class studentencap {
 public String name;
 public int age;
 public String gender;
 public int usn;
 
 public studentencap(String name,int age, String gender, int usn) {
	 this.name = name;
	 this.age = age;
	 this.gender = gender;
	 this.usn = usn;
 }
 
 public String getName() {
	 return name;
 }
 public int getAge() {
	 return age;
 }
 public String getgender() {
	 return gender;
 }
 public int getusn() {
	 return usn;
 }
}
